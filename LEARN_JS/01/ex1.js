// 1. 변수 선언과 할당
// 두 개의 변수 x,y를 선언하고, 10과 20을 할당, 두 수를 더한 값을 새로운 변수 sum에 저장 후 출력
let x = 10;
let y = 20;
sum = x + y;
console.log(sum);

// 2. 변수 재할당
// 변수 name에 Alice할당, Bob 재할당 후 콘솔 출력
let name = "Alice";
name = "Bob";
console.log(name);

// 3. 상수
// 상수 PI를 선언하고 값 3.14 할당, PI값 콘솔 출력
const PI = 3.14;
console.log(PI);

// 4. 변수 스왑
// a와 b를 선언하고 5와 10 할당, 값 교환 후 콘솔 출력
let a = 5;
let b = 10;
let swap = 0;
swap = a;
a = b;
b = swap;
console.log(`a : ` + a + `, b : ` + b);
