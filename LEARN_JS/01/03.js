// 02-dataType.js - 자료형과 값 교환
// -----------------------------------------------------------
//  자료형 (Data Type)
// - 값의 종류, 값이 어떤 성격을 가지는지를 정의
// - JavaScript는 동적 타입 언어 → 실행 시점에 자료형 결정
// -----------------------------------------------------------

// Q1. 숫자형(Number)
const x = 10;
const y = 20;
const sum = x + y;
console.log(sum); // 30

// Q2. 문자열(String)
let name = "Alice"; // 문자열 값
name = "Bob"; // 재할당 가능 (let 사용)
console.log(name); // Bob

// Q3. 상수(const)
const PI = 3.14; // 상수는 값 변경 불가
console.log(PI); // 3.14

// -----------------------------------------------------------
//  인터프리터 언어 vs 컴파일 언어
// - 인터프리터 언어: 코드가 "한 줄씩" 실행됨 (JavaScript, Python 등)
// - 컴파일 언어: 전체 코드를 "한 번에" 기계어로 변환 후 실행 (Java, C/C++)
// -----------------------------------------------------------

// Q4. 변수 값 교환 (swap)
let a = 5;
let b = 10;
let tmp;

tmp = a; // a값 보관
a = b; // a에 b값 대입
b = tmp; // b에 tmp값 대입

console.log(a, b); // 10 5

// 더 간단한 방법 (구조 분해 할당)
[a, b] = [b, a];
console.log(a, b); // 5 10

