// 10.js - 문자열도 유사 배열
// -----------------------------------------------------------
//  문자열
// - length, 인덱스로 접근 가능
// - 배열처럼 동작하는 유사 배열
// -----------------------------------------------------------

const a = "abc"; // 배열이 아님 → 유사 배열
console.log(a.length); // 3
console.log(a[0]); // a
console.log(a[1]); // b
console.log(a[2]); // c
