// -----------------------------------------------------------

const product = "가방"; // "가방", "옷", "신발", "반지"

if (product === "가방") {
  console.log("가방 로직");
} else if (product === "옷") {
  console.log("옷 로직");
} else if (product === "신발") {
  console.log("신발 로직");
} else if (product === "반지") {
  console.log("반지 로직");
} else {
  console.log("기타 로직");
}
