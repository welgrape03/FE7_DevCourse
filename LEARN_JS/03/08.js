// 08.js - 중첩 반복문, 시간 복잡도
// -----------------------------------------------------------
//  Big-O Notation
// - 반복문의 중첩 정도에 따라 실행 횟수 계산
// -----------------------------------------------------------

// 예시: 4중첩 for문 → 3*3*3*3 = 81번 실행
let n = 0;

for (let i = 1; i <= 3; i++) {
  for (let j = 1; j <= 3; j++) {
    for (let k = 1; k <= 3; k++) {
      for (let v = 1; v <= 3; v++) {
        n++;
      }
    }
  }
}
console.log(n); // 81
