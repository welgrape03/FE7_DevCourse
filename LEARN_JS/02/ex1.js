// 기본 산술 연산
let a = 10;
let b = 5;
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);

// 증가 및 감소 연산자
let y = 7;
console.log(y);
y++;
console.log(y);
y--;
console.log(y);

// 복합대입연산자
let x = 20;
x += 10;
x -= 5;
console.log(x);

// 비교 연산자
age1 = 25;
age2 = 30;
console.log(age1 > age2);
console.log(age1 < age2);
