// 05.js - 대입 연산자
// -----------------------------------------------------------
//  대입(할당) 연산자 (=)
// - 변수에 값을 할당할 때 사용
//  복합 대입 연산자
// +=, -=, *=, /=, %=, **=
// -----------------------------------------------------------

const num = 10;

let x = 10;
x += 10; // x = x + 10
x -= 10; // x = x - 10
x *= 10; // x = x * 10
x /= 10; // x = x / 10
x %= 10; // x = x % 10
x **= 2; // x = x ** 2
console.log(x);
